
DELETE FROM MASTER_TABLE M WHERE EXISTS (
    SELECT 1 FROM (SELECT EMP_ID, ROW_NUMBER() OVER (PARTITION BY EMP_ID ORDER BY EMP_ID) AS RN FROM MASTER_TABLE) D
    WHERE D.EMP_ID = M.EMP_ID AND D.RN > 1);
MERGE INTO MASTER_TABLE AS M
  USING (SELECT
        EMP_ID,
        MAX(EMP_NAME) AS EMP_NAME,
        MAX(AGE)      AS AGE,
        MAX(GENDER)   AS GENDER,
        MAX(SALARY)   AS SALARY_JULY
        FROM SEQUENTIAL_TABLE
        GROUP BY EMP_ID
  ) U ON (M.EMP_ID = U.EMP_ID) WHEN MATCHED THEN
    UPDATE SET SALARY_JULY = U.SALARY_JULY
  WHEN NOT MATCHED THEN
    INSERT (EMP_ID, EMP_NAME, AGE, GENDER, SALARY_MAY, SALARY_JUNE, SALARY_JULY)
    VALUES (U.EMP_ID, U.EMP_NAME, U.AGE, U.GENDER, NULL, NULL, U.SALARY_JULY);